#!/usr/bin/env Python3

# Okay, so we have these boxes called variables and also data that can go into the variable.
# This is called assignment, and you should not confuse the assignment equal sign (=) with "equality" in a mathematical sense here (that's what == will be used for later).

a = 1
print(a)
a = a + 1
print(a)
a = a * 2
print(a)