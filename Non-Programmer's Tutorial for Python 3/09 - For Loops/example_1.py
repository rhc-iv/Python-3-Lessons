#!/usr/bin/env python3

# With a 'for loop', we can repeat a piece of code. Let's look at an example:

one_to_ten = range(1, 11)
for count in one_to_ten:
    print(count)

# The output looks awfully familiar but the program code looks different. 
# The first line uses the range function. The range function uses two arguments 
# like this range(start, finish). start is the first number that is produced. 
# finish is one larger than the last number.